package hmh.rs.pojo;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class Material {

	/**
	 * Restriction Flag
	 */
	protected Boolean restrictionFlag;
	
	/**
	 * Plant name
	 */	
	protected String plantName;

	/**
	 * Material ID
	 */	
	protected String materialId;

	/**
	 * Quantity
	 */	
	protected Long quantity;

	/**
	 * List Price 
	 */
	protected Double listPrice;
	
	/**
	 * Net Price
	 */
	protected Double netPrice;
	
	
	
	
	public String getPlantName() {
		return plantName;
	}
	public void setPlantName(String plantName) {
		this.plantName = plantName;
	}
	public String getMaterialId() {
		return materialId;
	}
	public void setMaterialId(String materialId) {
		this.materialId = materialId;
	}
	public Long getQuantity() {
		return quantity;
	}
	public void setQuantity(Long quantity) {
		this.quantity = quantity;
	}
		
	// helper
	public void setQuantity(String s) {
		setQuantity(new Long(s));
	}
	
	/**
	 * @return the listPrice
	 */
	public Double getListPrice() {
		return listPrice;
	}
	/**
	 * @param listPrice the listPrice to set
	 */
	public void setListPrice(Double listPrice) {
		this.listPrice = listPrice;
	}
	/**
	 * @return the netPrice
	 */
	public Double getNetPrice() {
		return netPrice;
	}
	/**
	 * @param netPrice the netPrice to set
	 */
	public void setNetPrice(Double netPrice) {
		this.netPrice = netPrice;
	}
	public Boolean getRestrictionFlag() {
		return restrictionFlag;
	}
	public void setRestrictionFlag(Boolean restrictionFlag) {
		this.restrictionFlag = restrictionFlag;
	}
}
